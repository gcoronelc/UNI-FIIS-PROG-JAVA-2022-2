    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // Datos
            String sCodigo = txtCodigo.getText();
            String nombre = txtNombre.getText();
            AlumnoDto model = new AlumnoDto();
            int codigo;
            codigo = Integer.parseInt((sCodigo == null || sCodigo.length() == 0)?"0":sCodigo);
            model.setId(codigo);
            model.setNombre(nombre);
            // Proceso
            CrudAlumnoController control = new CrudAlumnoController();
            lista = control.readAll(model);
            // Fina
            pintarLista();
        } catch (Exception e) {
            Mensaje.error(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


