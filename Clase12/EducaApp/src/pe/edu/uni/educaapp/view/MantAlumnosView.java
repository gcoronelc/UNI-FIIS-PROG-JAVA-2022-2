    private void pintarLista() {
        DefaultTableModel tabla = (DefaultTableModel) tblDatos.getModel();
        tabla.setRowCount(0);
        for (AlumnoDto dto : lista) {
            Object[] rowData= {
                dto.getId(), dto.getNombre(),
                dto.getTelefono(), dto.getDireccion(),
                dto.getEmail()
            };
            tabla.addRow(rowData);
        }
    }
