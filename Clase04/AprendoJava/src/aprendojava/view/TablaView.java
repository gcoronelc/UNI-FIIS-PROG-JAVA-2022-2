    private void cleanForm() {
        cboTabla.setSelectedIndex(-1);
        DefaultTableModel tabla = (DefaultTableModel) tblResultado.getModel();
        tabla.setRowCount(0);
        cboTabla.requestFocus();
    }

    private void mostrarResultado(FilaDto[] resultado) {
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblResultado.getModel();
        tabla.setRowCount(0);
        for (FilaDto dto : resultado) {
            Object rowData[] = {dto.getBase(),
                "X", dto.getFactor(), "=", dto.getResultado()};
            tabla.addRow(rowData);
        }
    }
