    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        // Variables
        int tabla;
        FilaDto[] resultado;
        // Control de error
        int index = cboTabla.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione tabla.");
            return;
        }
        // Dato
        tabla = Integer.parseInt(cboTabla.getSelectedItem().toString());
        // Proceso
        Clase3Service service = new Clase3Service();
        resultado = service.calcularTabla(tabla);
        // Reporte
        mostrarResultado(resultado);
        setState(false);
    }//GEN-LAST:event_btnProcesarActionPerformed

